/// <summary>
/// Represents the different types of display units available in the Weather Monitoring Station.
/// </summary>
namespace WeatherStationClass.Enums;

public enum DisplayEnum
{
    /// <summary>
    /// Represents a display for showing current weather conditions.
    /// </summary>
    CurrentConditionsDisplay,

    /// <summary>
    /// Represents a display for showing weather forecasts.
    /// </summary>
    ForecastDisplay,

    /// <summary>
    /// Represents a display for showing weather statistics.
    /// </summary>
    StatisticsDisplay
}
